package com.cnnct.sys.vo;

import java.io.Serializable;

/**
 * 和用户相关的字段,用于存储到Session容器中的值对象
 *
 * @author XiongChun
 * @since 2010-01-13
 */
public class UserInfoVo implements Serializable {

    /**
     * 单位代码
     */
    private String grpId;

    /**
     * 单位名称
     */
    private String grpName;


    /**
     * 用户 状态
     */
    private String state;

    /**
     * 用户编号：varchar2(8) <Primary Key>
     */
    private String userid;

    /**
     * 用户名：varchar2(20)
     */
    private String username;

    /**
     * 登陆帐户：varchar2(20)
     */
    private String account;

    /**
     * 用户工号：varchar2(20)
     */
    private String perno;

    /**
     * 帐户类型：varchar2(20)
     */
    private String usertype;

    /**
     * 密码：varchar2(20)
     */
    private String password;

    /**
     * 性别(0:未知;1:男;2:女)：varchar2(2)
     */
    private String sex;

    /**
     * 部门编号：varchar2(60)
     */
    private String deptid;

    /**
     * 部门名称：varchar2(60)
     */
    private String deptName;

    /**
     * 锁定标志(0:锁定;1:激活)：varchar2(2)
     */
    private String lock;

    /**
     * 自定义主题
     */
    private String theme;

    /**
     * 自定义布局
     */
    private String layout;

    /****************权限信息*********************/
    /**
     * 用户数据权限
     */
    private String userPerm;

    /**
     * 数据权限角色编号
     */
    private String roleid;

    /**
     * 相对权限 (1,当前人员2,当前部门,3,分管分厂,4,分管部门,5,东奥所有)
     */
    private String relativeAuthority;

    /**
     * 订单权限(0-全部权限 1-部分权限)
     */
    private Integer prodAuthority;

    /**************登录信息****************/
    /**
     * 会话ID
     */
    private String sessionID;

    /**
     * 会话创建时间
     */
    private String sessionCreatedTime;

    /**
     * 登录IP
     */
    private String loginIP;

    /**
     * 浏览器
     */
    private String explorer;

    /**
     * Constractor
     */
    public UserInfoVo() {
    }

    /**
     * Constractor
     *
     * @param <code>userid</code>
     */
    public UserInfoVo(String userid) {
        this.userid = userid;
    }

    public String getUserid() {
        return this.userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getUsername() {
        return this.username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUsertype() {
        return usertype;
    }

    public void setUsertype(String usertype) {
        this.usertype = usertype;
    }

    public String getAccount() {
        return this.account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getPassword() {
        return this.password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSex() {
        return this.sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getDeptid() {
        return this.deptid;
    }

    public void setDeptid(String deptid) {
        this.deptid = deptid;
    }

    public String getLock() {
        return this.lock;
    }

    public void setLock(String lock) {
        this.lock = lock;
    }

    public String getTheme() {
        return theme;
    }

    public void setTheme(String theme) {
        this.theme = theme;
    }

    public String getLayout() {
        return layout;
    }

    public void setLayout(String layout) {
        this.layout = layout;
    }

    public String getSessionID() {
        return sessionID;
    }

    public void setSessionID(String sessionID) {
        this.sessionID = sessionID;
    }

    public String getLoginIP() {
        return loginIP;
    }

    public void setLoginIP(String loginIP) {
        this.loginIP = loginIP;
    }

    public String getSessionCreatedTime() {
        return sessionCreatedTime;
    }

    public void setSessionCreatedTime(String sessionCreatedTime) {
        this.sessionCreatedTime = sessionCreatedTime;
    }

    public String getExplorer() {
        return explorer;
    }

    public void setExplorer(String explorer) {
        this.explorer = explorer;
    }

    public String getGrpId() {
        return grpId;
    }

    public void setGrpId(String grpId) {
        this.grpId = grpId;
    }

    public String getGrpName() {
        return grpName;
    }

    public void setGrpName(String grpName) {
        this.grpName = grpName;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getUserPerm() {
        return userPerm;
    }

    public void setUserPerm(String userPerm) {
        this.userPerm = userPerm;
    }

    public String getRelativeAuthority() {
        return relativeAuthority;
    }

    public void setRelativeAuthority(String relativeAuthority) {
        this.relativeAuthority = relativeAuthority;
    }

    public String getRoleid() {
        return roleid;
    }

    public void setRoleid(String roleid) {
        this.roleid = roleid;
    }

    public Integer getProdAuthority() {
        return prodAuthority;
    }

    public void setProdAuthority(Integer prodAuthority) {
        this.prodAuthority = prodAuthority;
    }

    public String getPerno() {
        return perno;
    }

    public void setPerno(String perno) {
        this.perno = perno;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }
}
