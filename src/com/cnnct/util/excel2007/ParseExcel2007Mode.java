package com.cnnct.util.excel2007;

/**
 * 解析表的标志
 * @author zhouww
 * @since 2015-1-20
 */
public class ParseExcel2007Mode {
    
    // 忽视隐藏列   默认解析隐藏列
    public boolean ignoreHiddenCol = false;
    
    // 忽视隐藏行  默认解析隐藏行
    public boolean ignoreHiddenRow = false;
    
    // 忽视空行,如果不忽视空行 ，遇到空行解析结束
    public boolean ignoreEmptyRow = false;
    
    
    //~~~SET ADN GET
    public boolean isIgnoreHiddenCol() {
        return ignoreHiddenCol;
    }

    public void setIgnoreHiddenCol(boolean ignoreHiddenCol) {
        this.ignoreHiddenCol = ignoreHiddenCol;
    }

    public boolean isIgnoreHiddenRow() {
        return ignoreHiddenRow;
    }

    public void setIgnoreHiddenRow(boolean ignoreHiddenRow) {
        this.ignoreHiddenRow = ignoreHiddenRow;
    }

    public boolean isIgnoreEmptyRow() {
        return ignoreEmptyRow;
    }

    public void setIgnoreEmptyRow(boolean ignoreEmptyRow) {
        this.ignoreEmptyRow = ignoreEmptyRow;
    }
    
    
}
